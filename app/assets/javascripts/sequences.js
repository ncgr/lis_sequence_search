//
// Add example sequences and select appropriate blast dbs.
//
// Default example sequence functionality. If the example sequence links have
// the class 'special', the default behavior is modified slightly.
//
SEQUENCES.addSequences = function(id) {

  var self = this,
      sequence = $("#job_sequence"),
      blastn = $("#job_blastn_job_attributes_queue"),
      blastn_db = $("#job_blastn_job_attributes_blast_dbs option"),

      blastx = $("#job_blastx_job_attributes_queue"),
      blastx_db = $("#job_blastx_job_attributes_blast_dbs option"),

      tblastn = $("#job_tblastn_job_attributes_queue"),
      tblastn_db = $("#job_tblastn_job_attributes_blast_dbs option"),

      blastp = $("#job_blastp_job_attributes_queue"),
      blastp_db = $("#job_blastp_job_attributes_blast_dbs option"),

      tblastx = $("#job_tblastx_job_attributes_queue"),
      tblastx_db = $("#job_tblastx_job_attributes_blast_dbs option");

  if (id === "cdna") {
    sequence.removeClass("auto-hint").val(self.cdna);

    if (!$('#' + id).hasClass("special")) {
      blastn.prop("checked", true);
      $("#blastn").slideToggle();
      blastn_db.each(function() {
        $(this).attr("selected", "selected");
      });

      blastx.parent().show();
      blastx.prop("checked", true);
      $("#blastx").slideToggle();
      blastx_db.each(function() {
        $(this).attr("selected", "selected");
      });

      tblastx.parent().show();
      tblastx.prop("checked", true);
      $("#tblastx").slideToggle();
      tblastx_db.each(function() {
        $(this).attr("selected", "selected");
      });
    }
  }

  if (id === "prot") {
    sequence.removeClass("auto-hint").val(self.prot);

    if (!$('#' + id).hasClass("special")) {
      tblastn.prop("checked", true);
      $("#tblastn").slideToggle();
      tblastn_db.each(function() {
        $(this).attr("selected", "selected");
      });

      blastp.parent().show();
      blastp.prop("checked", true);
      $("#blastp").slideToggle();
      blastp_db.each(function() {
        $(this).attr("selected", "selected");
      });
    }
  }

  if (id === "mixed") {
    sequence.removeClass("auto-hint").val(self.mixed);

    if (!$('#' + id).hasClass("special")) {
      blastn.prop("checked", true);
      $("#blastn").slideToggle();
      blastn_db.each(function() {
        $(this).attr("selected", "selected");
      });

      blastx.parent().show();
      blastx.prop("checked", true);
      $("#blastx").slideToggle();
      blastx_db.each(function() {
        $(this).attr("selected", "selected");
      });

      tblastn.prop("checked", true);
      $("#tblastn").slideToggle();
      tblastn_db.each(function() {
        $(this).attr("selected", "selected");
      });

      blastp.parent().show();
      blastp.prop("checked", true);
      $("#blastp").slideToggle();
      blastp_db.each(function() {
        $(this).attr("selected", "selected");
      });

      tblastx.parent().show();
      tblastx.prop("checked", true);
      $("#tblastx").slideToggle();
      tblastx_db.each(function() {
        $(this).attr("selected", "selected");
      });
    }
  }
};

//
// Modifies search form for genome search.
//
// Enable blastn and tblastn selecting all species genome builds as targets.
//
SEQUENCES.setGenomeSearch = function() {

  var self = this,
      sequence = $("#job_sequence"),
      blastn = $("#job_blastn_job_attributes_queue"),
      blastn_db = $("#job_blastn_job_attributes_blast_dbs option"),

      blastx = $("#job_blastx_job_attributes_queue"),

      tblastn = $("#job_tblastn_job_attributes_queue"),
      tblastn_db = $("#job_tblastn_job_attributes_blast_dbs option"),

      blastp = $("#job_blastp_job_attributes_queue");

      tblastx = $("#job_tblastx_job_attributes_queue"),
      tblastx_db = $("#job_tblastx_job_attributes_blast_dbs option"),

  blastx.parent().hide();
  blastp.parent().hide();

  blastn.prop("checked", true);
  blastn_db.each(function() {
    if ($(this).val().search(/genome/) >= 0) {
      $(this).attr("selected", "selected");
    } else {
      $(this).remove();
    }
  });

  tblastn.prop("checked", true);
  tblastn_db.each(function() {
    if ($(this).val().search(/genome/) >= 0) {
      $(this).attr("selected", "selected");
    } else {
      $(this).remove();
    }
  });

  tblastx.prop("checked", true);
  tblastx_db.each(function() {
    if ($(this).val().search(/genome/) >= 0) {
      $(this).attr("selected", "selected");
    } else {
      $(this).remove();
    }
  });

};

