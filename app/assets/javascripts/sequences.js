// Call correct search method based on query.
//
SEQUENCES.checkQuery = function(query) {

  var self = this;

  switch(query) {
    case "genome":
      self.addSpecialClass();
      self.setGenomeSearch();
      break;
    case "gene":
      self.addSpecialClass();
      self.setGeneSearch();
      break;
    default:
      self.removeSpecialClass();
      break;
  }

};

$(function() {

  // Check for special settings query string.
  //
  // ?genome = genome settings
  // ?gene = gene settings
  var query = document.location.search.slice(1);
  if (!_.isEmpty(query)) {
    SEQUENCES.checkQuery(query.toLowerCase());
  } else {
    SEQUENCES.removeSpecialClass();
  }

  // Helper to clear form after click event.
  function clearForm() {
    $('textarea').val('');
    $('input:text').val('');
    $('input:file').val('');
    $('input:checkbox').prop('checked', false);
    $('select').val('');

    $('.toggle').each(function() {
      $(this).hide();
    });

    $('form :input.auto-hint').autoHint('addHints');
  }

  // Bind click event to each example sequence.
  // If an element has the class 'special', don't clear the form.
  $("#example-sequences a").click(function() {
    var id = ($(this).attr("id"));
    if (!$(this).hasClass("special")) {
      clearForm();
    }
    SEQUENCES.addSequences(id);
  });

});
